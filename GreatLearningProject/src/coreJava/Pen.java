package coreJava;

public class Pen {
	Pen(String color){
		System.out.println("Cost of pen = 15 Rs");
		System.out.println("Colour of pen is:" +color);
	}
	Pen(String color, String company){
		System.out.println("Cost of one pen 15 Rs");
		System.out.println("Colour of pen is : "+color);
		System.out.println("Company of pen is:" +company);
	}

}
